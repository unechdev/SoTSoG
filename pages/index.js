import Head from "next/head";
import Image from "next/image";
import { useState } from "react";
import Select from "react-select";
import styles from "../styles/Home.module.css";

const TranslatedBookComponent = () => {
  const translated_book_list = [
    "author",
    "year",
    "title",
    "translator",
    "publisher",
  ];

  const [results, setResults] = useState({
    author: "著者",
    year: "2022",
    title: "タイトル",
    translator: "訳者",
    publisher: "出版社",
  });
  return (
    <>
      <h2>訳本</h2>
      {translated_book_list.map((text) => {
        return (
          <input
            placeholder={text}
            onChange={(e) => setResults({ ...results, [text]: e.target.value })}
            key={text}
          ></input>
        );
      })}
      <p
        onClick={(e) => {
          navigator.clipboard.writeText(e.target.textContent);
        }}
      >
        {`${results.author}(${results.year})『${results.title}』${results.translator}、${results.publisher}。`}
      </p>
      <p className=" text-sm text-gray-400">
        テキストをクリックするとコピーできます。
      </p>
    </>
  );
};

const Generator = ({ method }) => {
  switch (method) {
    case "translated_book":
      return <TranslatedBookComponent />;
  }
};
